// Operadores Aritméticos

// Multiplicação *
console.log(3 * 5)
// Divisão /
console.log(12 / 2)
// Soma +
console.log(34 + 67)
// Subtração -
console.log(0 - 1)

// Resto da divisão %
console.log(11 % 9)
// Incremento ++
let increment = 0
increment++
console.log(increment)
// Decremento --
let decrement = 0
decrement--
console.log(--decrement)
// Exponencial **
console.log(3 ** 2)
