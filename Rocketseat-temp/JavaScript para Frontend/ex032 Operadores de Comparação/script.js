// Operadores de Comparação

// Irá comparar valores e retornar um Boolean como resposta à comparação

let one = 1, two = 2

// == igual a
console.log(two == 1)
console.log(one == 1)

// != Diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != '1')

// === Estritamente igual a
console.log(one === '1')
console.log(one === 1)

// !== Estritamente diferente de
console.log(two !== '2')
console.log(two !== 2)


// > Maior que
console.log(one > two) // false
// >= Maior igual a
console.log(one >= 1)
// < Menor que
console.log(one < two)

// <= Menor igual a
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
